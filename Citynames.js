function city_country(city, country) {
    return city + ", " + country;
}
// Call the function with city-country pairs
var pair1 = city_country("Lahore", "Pakistan");
var pair2 = city_country("Tokyo", "Japan");
var pair3 = city_country("Rome", "Italy");
// Print the returned values
console.log(pair1);
console.log(pair2);
console.log(pair3);
